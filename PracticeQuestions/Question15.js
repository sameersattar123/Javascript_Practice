// program for a simple calculator

let num1 = 5;
let num2 = 9;
let operator = "/";
let result;

if (operator == "+") {
 console.log(result = num1 + num2);
} else if (operator == "-") {
 console.log(result = num1 - num2);
} else if (operator == "*") {
 console.log(result = num1 * num2);
} else if (operator == "/") {
 console.log(result = num1 / num2);
} else {
  console.log("Operator does not exist");
}
